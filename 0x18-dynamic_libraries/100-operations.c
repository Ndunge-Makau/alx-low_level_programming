#include <stdio.h>

/**
 * add - Adds two integers
 * @a: First Integer
 * @b: Second integer
 * Return: Sum of the two integers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtracts two integers
 * @a: First Integer
 * @b: Second integer
 * Return: Difference of the two integers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies two integers
 * @a: First Integer
 * @b: Second integer
 * Return: Product of the two integers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides two integers
 * @a: First Integer
 * @b: Second integer
 * Return: Result of dividing the two integers
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Finds modulus two integers
 * @a: First Integer
 * @b: Second integer
 * Return: Modulus of the two integers
 */

int mod(int a, int b)
{
	return (a % b);
}

